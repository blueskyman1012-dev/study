export function setLanguage(lang) {
  if (translations[lang]) {
    currentLang = lang;
    safeSetItem(STORAGE_KEY, lang);
    applyToHTML();
  }
}

export function applyToHTML() {
  document.title = t('htmlTitle');
  const h1 = document.querySelector('#login-box h1');
  if (h1) h1.textContent = t('htmlTitle');
  const p = document.querySelector('#login-box p');
  if (p) p.textContent = t('htmlSlogan');
  const regBtn = document.getElementById('register-btn');
  if (regBtn) regBtn.textContent = t('htmlRegister');
  const logoutBtn = document.getElementById('logout-btn');
  if (logoutBtn) logoutBtn.textContent = t('htmlLogout');
  const captureBtn = document.getElementById('camera-capture');
  if (captureBtn) captureBtn.textContent = t('htmlCapture');
  const closeBtn = document.getElementById('camera-close');
  if (closeBtn) closeBtn.textContent = t('htmlClose');
}

export function getLanguage() {
  return currentLang;
}

export function t(key, ...args) {
  const dict = translations[currentLang] || translations.ko;
  let text = dict[key] ?? translations.ko[key] ?? key;
  // Replace {0}, {1}, ... placeholders
  args.forEach((arg, i) => {
    text = text.replace(`{${i}}`, arg);
  });
  return text;
}
